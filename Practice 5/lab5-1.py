# Запит користувача на введення кількості елементів у масиві
N = int(input("Введіть кількість елементів у масиві: "))

# Ініціалізуємо порожній масив для зберігання введених елементів
arr = []

# Запит користувача на введення елементів масиву
for i in range(N):
    element = int(input(f"Введіть {i+1}-й елемент: "))
    arr.append(element)

# Ініціалізуємо змінну для зберігання суми парних індексів
sum_of_even_indices = 0

# Обчислюємо суму елементів з парними індексами
for i in range(0, N, 2):
    sum_of_even_indices += arr[i]

# Виводимо результат
print(f"Сума елементів з парними індексами: {sum_of_even_indices}")
