# Зчитуємо чотиризначне натуральне число n від користувача
n = int(input("Введіть чотиризначне натуральне число n: "))

# Перевіряємо, чи n дійсно є чотиризначним натуральним числом
if 1000 <= n <= 9999:
    # Перетворюємо число n на рядок для роботи з цифрами
    n_str = str(n)
    
    # Знаходимо найбільшу цифру, використовану в запису числа n
    max_digit = max(n_str)
    
    # Виводимо результат
    print(f"Найбільша цифра в числі {n} - це {max_digit}.")
else:
    print("Введене число не є чотиризначним натуральним числом.")
